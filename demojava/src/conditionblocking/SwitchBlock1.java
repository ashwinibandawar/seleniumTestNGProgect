package conditionblocking;

public class SwitchBlock1 {
	public static void main(String[] args) {

		char Car = 'a';

		switch(Car) {

		case 'a':
			System.out.println("Nano");

		case 'b':
			System.out.println("BMW");
            break;
		case 'c':
			System.out.println("Dust");
			break;
		case 'd':
			System.out.println("Coupe");

		}
	}
}