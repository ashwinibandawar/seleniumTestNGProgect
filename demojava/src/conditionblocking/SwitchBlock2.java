package conditionblocking;

public class SwitchBlock2 {
public static void main(String[] args) {
	
	
	
	
	
	
	
	char colours=1;
	switch(colours) {
	
	case 1:
		System.out.println("red");
	
	case 2:
		System.out.println("blue");
		break;
	case 3:
		System.out.println("black");
		
	case 4:
		System.out.println("orange");
	
	
	
}
}
}